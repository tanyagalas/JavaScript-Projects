function performAddition() {
    var num1 = 5;
    var num2 = 10;
    var result = num1 + num2;
    document.getElementById("Math").innerHTML = "The result of the addition operation is: " + result;
  }
  
  function performSubtraction() {
    var num1 = 10;
    var num2 = 5;
    var result = num1 - num2;
    document.getElementById("Math").innerHTML = "The result of the subtraction operation is: " + result;
  }